package MeetWhen.vo;

public class AdressVO {
	private double Sx;
	private double Sy;
	private double Nx;
	private double Ny;
	public double getSx() {
		return Sx;
	}
	public void setSx(double sx) {
		Sx = sx;
	}
	public double getSy() {
		return Sy;
	}
	public void setSy(double sy) {
		Sy = sy;
	}
	public double getNx() {
		return Nx;
	}
	public void setNx(double nx) {
		Nx = nx;
	}
	public double getNy() {
		return Ny;
	}
	public void setNy(double ny) {
		Ny = ny;
	}
	
	
}
